function repeatStringNumTimes(str, num) {
  // repeat after me
  
  //initialized empty string
  var string = "";
  
  //if num is positive
  if(num > 0){
  
      //traverse the array num times
      for(var i = 0; i < num; i++){
        
        //concatinate add string value into string
        string += str.concat();
        
      }
      
    //return string
    return string;
  }else{
  
    //else return nothing
    return "";
  }
  

}

repeatStringNumTimes("abc", 3);
